// ─── UPDATE merge block ───────────────────────────────────────────────────────

export function buildUpdateBlock(
  existingContent: string,
  ctx: StubContext
): string {
  const fence = "=".repeat(60);
  const timestamp = new Date().toISOString();
  return [
    existingContent,
    ``,
    `// ${"─".repeat(58)}`,
    `// MERGE UPDATE — ${timestamp}`,
    `// Decision: ${ctx.metadata["decisionId"] ?? "unknown"}`,
    `// Source: ${ctx.path}`,
    `// Reason: ${String(ctx.metadata["reason"] ?? "content update from manifest")}`,
    `// ${fence}`,
    `// TODO: review and integrate the following changes from the merged manifest.`,
    `// The original content above is preserved; apply selective updates below.`,
    `// ${"─".repeat(58)}`,
  ].join("\n");
}

