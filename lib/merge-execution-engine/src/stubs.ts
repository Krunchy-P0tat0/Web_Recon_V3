// ─── Dispatch ─────────────────────────────────────────────────────────────────

export function generateCreateStub(ctx: StubContext): { content: string; ext: string } {
  const fw = ctx.framework.toLowerCase();

  switch (ctx.entityKind) {
    case "route":
    case "layout": {
      if (fw === "nextjs") {
        const content = ctx.entityKind === "layout"
          ? nextjsLayoutStub(ctx)
          : nextjsPageStub(ctx);
        return { content, ext: "tsx" };
      }
      if (fw === "astro") {
        const content = astroPageStub(ctx);
        return { content, ext: "astro" };
      }
      if (fw === "react") {
        const content = ctx.entityKind === "layout"
          ? reactLayoutStub(ctx)
          : reactPageStub(ctx);
        return { content, ext: "tsx" };
      }
      return { content: htmlPageStub(ctx), ext: "html" };
    }

    case "component": {
      if (fw === "astro") {
        return { content: astroComponentStub(ctx), ext: "astro" };
      }
      return { content: reactComponentStub(ctx), ext: "tsx" };
    }

    case "api": {
      if (fw === "nextjs") {
        return { content: nextjsApiStub(ctx), ext: "ts" };
      }
      return { content: expressApiStub(ctx), ext: "ts" };
    }

    case "datasource": {
      return { content: dataSourceStub(ctx), ext: "ts" };
    }

    default: {
      return { content: reactComponentStub(ctx), ext: "tsx" };
    }
  }
}

// ─── Path derivation ──────────────────────────────────────────────────────────

export function deriveFilePath(
  ctx: StubContext,
  ext: string
): string {
  const fw = ctx.framework.toLowerCase();
  const slug = toSlug(ctx.name);
  const srcPath = ctx.path && ctx.path !== "/" ? ctx.path : `/${slug}`;

  switch (ctx.entityKind) {
    case "route":
      if (fw === "nextjs") return `app${srcPath}/page.${ext}`;
      if (fw === "astro") return `src/pages${srcPath}.${ext}`;
      return `src/pages${srcPath}.${ext}`;

    case "layout":
      if (fw === "nextjs") return `app${srcPath}/layout.${ext}`;
      return `src/layouts/${toComponentName(ctx.name)}.${ext}`;

    case "component":
      return `src/components/${toComponentName(ctx.name)}.${ext}`;

    case "api":
      if (fw === "nextjs") return `app/api${srcPath}/route.${ext}`;
      return `src/routes/api${srcPath}.${ext}`;

    case "datasource":
      return `src/lib/${toCamelCase(ctx.name)}.${ext}`;

    default:
      return `src/generated/${slug}.${ext}`;
  }
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function toComponentName(raw: string): string {
  return raw
    .replace(/[^a-zA-Z0-9\s_-]/g, "")
    .split(/[\s_-]+/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join("");
}

function toSlug(raw: string): string {
  return raw
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .trim()
    .replace(/[\s]+/g, "-");
}

function toCamelCase(raw: string): string {
  const parts = raw
    .replace(/[^a-zA-Z0-9\s_-]/g, "")
    .split(/[\s_-]+/);
  return parts
    .map((w, i) =>
      i === 0
        ? w.toLowerCase()
        : w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()
    )
    .join("");
}
